# ==============================================================================
# ФАЙЛ: setup_filter.ps1
# ЧТО ДЕЛАЕТ: один раз настраивает git-фильтр в ЭТОМ репозитории.
#             После этого при каждом git add / git commit комментарии
#             будут автоматически вырезаться из версии, которая уходит
#             в git и на GitHub — а файл у тебя на диске останется
#             с комментариями, его никто не трогает.
#
# ЗАПУСКАТЬ ОДИН РАЗ (после клонирования/настройки проекта):
#   .\setup_filter.ps1
#
# Настройка пишется в .git/config — этот файл НЕ версионируется
# и НЕ пушится на GitHub, так что сама эта "магия" видна только тебе.
# ==============================================================================

Write-Host "Настраиваю git-фильтр для очистки комментариев..."

# clean — что делать с файлом, когда он идёт В репозиторий (git add/commit)
git config filter.stripcomments.clean "python strip_comments.py"

# smudge — что делать с файлом, когда он идёт ИЗ репозитория в рабочую
# папку (git checkout). "cat" значит "ничего не делай, отдай как есть" —
# специально НЕ пытаемся восстановить комментарии, это невозможно,
# они физически не хранятся в git.
git config filter.stripcomments.smudge "cat"

Write-Host "Готово. Применяю фильтр к уже отслеживаемым файлам..."

# renormalize — прогоняет фильтр по ВСЕМ уже закоммиченным файлам,
# а не только по новым. Без этого шага старые файлы в репозитории
# останутся с комментариями, пока их кто-то не изменит.
git add --renormalize .

Write-Host ""
Write-Host "Готово! Дальше работай как обычно:"
Write-Host "  git add ."
Write-Host "  git commit -m '...'"
Write-Host "  git push"
Write-Host ""
Write-Host "Комментарии останутся только у тебя на диске."
Write-Host "Проверить, что реально уйдёт в GitHub, можно так:"
Write-Host "  git show :apps/listings/models/apartment.py"
